public class CreatingMethod{
    public int twice(int number){
        return number*2;
    }
    public boolean isBig(int number){
        return number>10000;
    }
    public void printResult(int number){
        System.out.println(number);
    }
    public boolean inOrder(int number1,int number2){
        return number1<number2;
    }
    public int increment(int number){
        return number+1;
    }
    public double half(double number){
        return number/2.0;
    }
}